    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        if (modoOper == 0) {
            Genero genero = new Genero();
            genero.setNome(escreverNomeGen.getText());

            GeneroBLL.create(genero);

            feedbackMessage.setText("GENERO CRIADO COM SUCESSO!");
            escreverNomeGen.setText("");

        }else{
            //Modo Update
            genToUpdte.setNome(escreverNomeGen.getText());

            GeneroBLL.update(genToUpdte);
            feedbackMessage.setText("GENERO ATUALIZADO COM SUCESSO!");
            escreverNomeGen.setText("");
        }
    }//GEN-LAST:event_okActionPerformed

