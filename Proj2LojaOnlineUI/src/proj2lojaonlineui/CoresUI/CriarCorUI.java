    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        // TODO add your handling code here:

        if (modoOper == 0) {
            Cor cor = new Cor();
            cor.setNome(escreverCor.getText());

            CorBLL.create(cor);

            feedbackMessage.setText("COR CRIADA COM SUCESSO!");
            escreverCor.setText("");
            
        }
        else if (modoOper == 1){
            //Modo Update
            cor1ToUpdte.setNome(escreverCor.getText());
            
            CorBLL.update(cor1ToUpdte);
            feedbackMessage.setText("COR EDITADA COM SUCESSO!");
            escreverCor.setText("");
          
        }
        else{
            //Modo Delete
            cor1ToDelete.setNome(escreverCor.getText());
            
            try{
                CorBLL.delete(cor1ToDelete);
                feedbackMessage.setText("COR EXCLUIDA COM SUCESSO!");
            }
            catch(Exception ex){
                feedbackMessage.setText("ERRO! Cor NÃ£o pode ser Apagada!");
            }
            
            escreverCor.setText("");
        }
        
        
        
        
       
    }//GEN-LAST:event_OKActionPerformed

